/**
 * Redirects the user to a specified URL after a delay of 6 seconds.
 * 
 * This function uses `setTimeout` to delay the execution of the redirect. 
 * After 6000 milliseconds (6 seconds), the browser will navigate to the given URL.
 */
setTimeout(function() {
    window.location.href = 'https://deanophp.github.io/quizzy-msp2/';
}, 6000); 
